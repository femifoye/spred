    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $validated = $request->validate(['email'=>'required|email|unique:subscriptions']);
        $subscription = new Subscription;
        $subscription->email = $validated['email'];
        $subscription->save();
        Cookie::queue('email', $subscription->email, 90000);
        return redirect('/thanks-for-subscribing')->with('email', $subscription->email);
    }

